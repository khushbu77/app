package com.example.retrivedata;

public class Votes {


    private String title;

    private double rating;

    private String image;

    public Votes( String title, double rating, String image) {

        this.title = title;

        this.rating = rating;

        this.image = image;
    }



    public String getTitle1() {
        return title;
    }



    public double getRating1() {
        return rating;
    }



    public String getImage1() {
        return image;
    }
}
